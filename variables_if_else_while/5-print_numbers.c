#include <stdlib.h>
#include <time.h>
#include <stdio.h> /* Missing header for printf */
/**
 * main - Entry point
 *
 * Description: Generates a random number and determines if it's
 * positive, negative, or zero.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	char ch;

	for (ch = '0'; ch <= '9'; ch++)
		putchar(ch);
	{
		putchar('\n');
	}
	return (0);
}
